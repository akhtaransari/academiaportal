package com.leucine.academiaportal.enums;

/**
 * Enum representing the various roles in the application.
 * This enum defines the different types of users and their associated roles.
 */
public enum Role {

    /**
     * Represents a student role.
     * Users with this role have permissions and functionalities specific to students.
     */
    STUDENT,

    /**
     * Represents a faculty member role.
     * Users with this role have permissions and functionalities specific to faculty members.
     */
    FACULTY_MEMBER,

    /**
     * Represents an administrator role.
     * Users with this role have administrative privileges and functionalities.
     */
    ADMINISTRATOR
}
